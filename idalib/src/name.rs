/// Set a name at the specified address.
///
/// # Arguments
/// * `ea` - Linear address
/// * `name` - New name (empty string to delete)
/// * `flags` - Set name flags
pub fn set_name(ea: Address, name: &str, flags: SetNameFlags) -> bool {
    let c_name = match CString::new(name) {
        Ok(s) => s,
        Err(_) => return false,
    };
    unsafe { idalib_set_name(ea.into(), c_name.as_ptr(), flags.bits().into()) }
}

/// Delete name at the specified address.
pub fn del_name(ea: Address) -> bool {
    unsafe { idalib_del_name(ea.into()) }
}

/// Force a name at the specified address.
///
/// This function always succeeds by adding a numeric suffix if necessary.
pub fn force_name(ea: Address, name: &str, flags: SetNameFlags) -> bool {
    let c_name = match CString::new(name) {
        Ok(s) => s,
        Err(_) => return false,
    };
    unsafe { idalib_force_name(ea.into(), c_name.as_ptr(), flags.bits().into()) }
}

/// Get name at the specified address.
pub fn get_name(ea: Address) -> Option<String> {
    let name = unsafe { idalib_get_name(ea.into()) };
    if name.is_empty() { None } else { Some(name) }
}

/// Get visible name (may be demangled) at the specified address.
pub fn get_visible_name(ea: Address) -> Option<String> {
    let name = unsafe { idalib_get_visible_name(ea.into()) };
    if name.is_empty() { None } else { Some(name) }
}

/// Get short name (shortest form) at the specified address.
pub fn get_short_name(ea: Address) -> Option<String> {
    let name = unsafe { idalib_get_short_name(ea.into()) };
    if name.is_empty() { None } else { Some(name) }
}

/// Get long name (demangled form) at the specified address.
pub fn get_long_name(ea: Address) -> Option<String> {
    let name = unsafe { idalib_get_long_name(ea.into()) };
    if name.is_empty() { None } else { Some(name) }
}

/// Get colored name at the specified address (includes color codes).
pub fn get_colored_name(ea: Address) -> Option<String> {
    let name = unsafe { idalib_get_colored_name(ea.into()) };
    if name.is_empty() { None } else { Some(name) }
}

/// Get address by name.
///
/// # Arguments
/// * `from` - Starting address for name resolution (use 0 for global search)
/// * `name` - Name to look up
pub fn get_name_ea(from: Address, name: &str) -> Option<Address> {
    let c_name = match CString::new(name) {
        Ok(s) => s,
        Err(_) => return None,
    };
    let addr = unsafe { idalib_get_name_ea(from.into(), c_name.as_ptr()) };
    if addr == BADADDR {
        None
    } else {
        Some(addr.into())
    }
}

