// Name Setting Flags
// =============================================================================

bitflags! {
    /// Flags for `set_name` and `force_name` operations.
    #[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
    pub struct SetNameFlags: i32 {
        /// Don't display warning if failed
        const NOWARN = 0x0001;
        /// Replace invalid chars with '_'
        const AUTO = 0x0002;
        /// Fail if the name contains invalid chars
        const CHECK = 0x0004;
        /// If name is public, make it weak
        const WEAK = 0x0008;
        /// Don't mark name as non-autogenerated
        const NON_AUTO = 0x0010;
        /// Local name
        const LOCAL = 0x0020;
        /// Make name public
        const PUBLIC = 0x0040;
        /// Make name non-public
        const NON_PUBLIC = 0x0080;
        /// Make name non-weak
        const NON_WEAK = 0x0100;
        /// Don't allow demangled name
        const NODEMANGLE = 0x0200;
        /// Don't create tail chunks
        const DELTAIL = 0x0400;
        /// Get/set debug name
        const IDBENC = 0x0800;
        /// force-name: remove library function attribute if set
        const FORCE = 0x1000;
        /// Ignore external names
        const NODUMMY = 0x2000;
    }
}

