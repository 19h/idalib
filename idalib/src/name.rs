bitflags! {
    /// Flags for disabling parts of demangling.
    #[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
    pub struct DemangleFlags: u32 {
        /// Disable return type demangling
        const NO_RETURN_TYPE = 0x0001;
        /// Disable argument demangling
        const NO_ARGUMENTS = 0x0002;
        /// Disable const/volatile modifiers
        const NO_CV = 0x0004;
        /// Disable scope resolution
        const NO_SCOPE = 0x0008;
        /// Disable access specifiers
        const NO_ACCESS = 0x0010;
        /// Disable throwing specification
        const NO_THROW = 0x0020;
        /// Disable special prefix demangling
        const NO_SPECIAL = 0x0040;
        /// Disable ptr64 specifier
        const NO_PTR64 = 0x0080;
        /// Disable unaligned specifier
        const NO_UNALIGNED = 0x0100;
        /// Disable restrict specifier
        const NO_RESTRICT = 0x0200;
        /// Don't add 'unsigned' to 'char'
        const NO_USIGNED_CHAR = 0x0400;
        /// Disable anonymous namespace
        const NO_ANONYMOUS = 0x0800;
        /// Disable macros
        const NO_MACROS = 0x1000;
    }
}

/// Demangle a name.
///
/// # Arguments
/// * `name` - Mangled name
/// * `disable_mask` - Flags to disable specific parts of demangling
pub fn demangle_name(name: &str, disable_mask: DemangleFlags) -> Option<String> {
    let c_name = match CString::new(name) {
        Ok(s) => s,
        Err(_) => return None,
    };
    let result = unsafe { idalib_demangle_name(c_name.as_ptr(), disable_mask.bits()) };
    if result.is_empty() {
        None
    } else {
        Some(result)
    }
}

/// Demangle a name with default settings.
pub fn demangle(name: &str) -> Option<String> {
    demangle_name(name, DemangleFlags::empty())
}

