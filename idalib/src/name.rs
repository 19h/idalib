bitflags! {
    /// Flags for cleanup_name.
    #[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Hash)]
    pub struct CleanupNameFlags: u32 {
        /// Allow leading underscores
        const ALLOW_LEADING_UNDERSCORE = 0x0001;
        /// Allow leading at-signs
        const ALLOW_LEADING_AT = 0x0002;
        /// Convert to lowercase
        const TO_LOWER = 0x0004;
    }
}

/// Clean up a name to make it a valid identifier.
///
/// # Arguments
/// * `ea` - Linear address (for context)
/// * `name` - Name to clean up
/// * `flags` - Cleanup flags
pub fn cleanup_name(ea: Address, name: &str, flags: CleanupNameFlags) -> Option<String> {
    let c_name = match CString::new(name) {
        Ok(s) => s,
        Err(_) => return None,
    };
    let result = unsafe { idalib_cleanup_name(ea.into(), c_name.as_ptr(), flags.bits()) };
    if result.is_empty() {
        None
    } else {
        Some(result)
    }
}
